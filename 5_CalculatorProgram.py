# Sistem Calculator

nilai = int(input("Masukan angka pertama : "))
nilai1 = int(input("Masukan angka kedua : "))

penjumlahan = nilai + nilai1
pengurangan = nilai - nilai1
perkalian = nilai * nilai1
pembagian = nilai / nilai1
perpangkatan = nilai ** nilai1

print("Hasil dari penjumlahan adalah ", penjumlahan)
print("Hasil dari perngurangan adalah ", pengurangan)
print("Hasil dari perkalian adalah ", perkalian)
print("Hasil dari pembagian adalah ", pembagian)
print("Hasil dari perpangkatan adalah ", perpangkatan)

