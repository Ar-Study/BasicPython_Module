# Lambda adalah suatu ekspresi atau perintah untuk membuat function
# Pembuatan Function dengan Lambda
# lambda argument/parameter : code yang ingin dijalankan
lambda x,y : x**2 + y**2

# Contoh
greeting = lambda name: print(f"Hello, {name}") 
greeting("Coding First")

# Eksekusi Function Secara Langsung
(lambda x,y: x**2 + y**2)(4,6)